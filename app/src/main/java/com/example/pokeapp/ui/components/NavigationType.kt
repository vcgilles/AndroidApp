package com.example.pokeapp.ui.components

/**
 * Enum class representing different types of navigation within the PokeApp application.
 *
 * - [BOTTOM_NAVIGATION]: Represents bottom navigation.
 * - [NAVIGATION_RAIL]: Represents navigation using a navigation rail.
 */
enum class NavigationType {
    /**
     * Represents bottom navigation.
     */
    BOTTOM_NAVIGATION,
    /**
     * Represents navigation using a navigation rail.
     */
    NAVIGATION_RAIL
}