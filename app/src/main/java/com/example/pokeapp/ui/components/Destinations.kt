package com.example.pokeapp.ui.components

/**
 * Enum class representing different destinations within the PokeApp application.
 *
 * - [Start]: Represents the start destination.
 * - [PokemonList]: Represents the Pokemon list destination.
 * - [PokemonDetail]: Represents the Pokemon detail destination.
 */
enum class Destinations {
    Start,
    PokemonList,
    PokemonDetail
}